def get_year_code(year: int) -> int:
    """Get the year code for a given year (1800-2099)."""
    if 1800 <= year < 1900:
        return C19[year - 1800]
    elif 1900 <= year < 2000:
        return C20[year - 1900]
    elif 2000 <= year < 2100:
        return C21[year - 2000]
    else:
        raise ValueError(f"No data for year {year}")
